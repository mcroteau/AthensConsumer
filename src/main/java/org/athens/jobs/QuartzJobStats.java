package org.athens.jobs;

public class QuartzJobStats {

    private int total;
    private int count;
    private int found;
    private int saved;


    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }


    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }


    public int getFound() {
        return found;
    }

    public void setFound(int found) {
        this.found = found;
    }


    public int getSaved() {
        return saved;
    }

    public void setSaved(int saved) {
        this.saved = saved;
    }

}
